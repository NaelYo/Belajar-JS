/*Mathematics in JavaScript*/

//Mathematics dengan tipedata Number
let x = 12;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

//Mathematics dengan tipedata BigInt
let a = 15n
let b = 5n

console.log(a + b);
console.log(a + b);
console.log(a + b);
console.log(a + b);
console.log(a + b);

//Mathematics dengan tipedata String
let i = '1';
let j = '2';

console.log(i + j);
console.log(i - j);
console.log(i * j);
console.log(i / j);
console.log(i % j);

//Mathematics String with Number
let m = '1'
